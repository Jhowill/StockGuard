type TranslationTree = (typeof translations)[keyof typeof translations];

function getValue(tree: TranslationTree, path: string) {
  return path.split('.').reduce<unknown>((current, segment) => {
    if (current && typeof current === 'object' && segment in current) {
      return (current as Record<string, unknown>)[segment];
    }

    return undefined;
  }, tree);
}

export function useI18n() {
  const { language, setLanguage, currency } = useAppState();

  const currentLanguage: keyof typeof translations =
    language === 'system' ? 'pt-BR' : language;
  const dictionary: TranslationTree = translations[currentLanguage];

  return useMemo(
    () => ({
      language: currentLanguage,
      currencyLabel: currency,
      setLanguage,
      t: (key: string) => {
        const value = getValue(dictionary, key);
        return typeof value === 'string' ? value : key;
      },
    }),
    [currency, currentLanguage, dictionary, setLanguage],
  );
}
