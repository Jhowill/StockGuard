function getValue(tree: TranslationTree, path: string) {
  return path.split('.').reduce<unknown>((current, segment) => {
    if (current && typeof current === 'object' && segment in current) {
      return (current as Record<string, unknown>)[segment];
    }

    return undefined;
  }, tree);
}

function interpolate(value: string, params?: Params) {
  if (!params) {
    return value;
  }

  return value.replace(/\{\{(\w+)\}\}/g, (_, key: string) => String(params[key] ?? ''));
}

export function useI18n() {
  const { language, setLanguage, currency } = useAppState();

  const currentLanguage: Locale = language === 'system' ? 'pt-BR' : language;
  const dictionary = translationsWithFallback[currentLanguage];

  return useMemo(
    () => ({
      language: currentLanguage,
      currencyLabel: currency,
      setLanguage,
      t: (key: string, params?: Params) => {
        const value = getValue(dictionary, key) ?? getValue(translationsWithFallback['pt-BR'], key);
        return typeof value === 'string' ? interpolate(value, params) : key;
      },
    }),
    [currency, currentLanguage, dictionary, setLanguage],
  );
}
